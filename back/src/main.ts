async function bootstrap() {
	const app = await NestFactory.create(AppModule, {
		logger: console,
	})
	app.enableCors({
		allowedHeaders: "*",
		origin: "*",
		methods: "GET,HEAD,PUT,PATCH,POST,DELETE",
		credentials: false,
	})
	await app.listen(3000) // TODO : env > PORT
}
bootstrap()
