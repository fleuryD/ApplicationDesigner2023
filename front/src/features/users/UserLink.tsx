import React from "react"
import { Link } from "react-router-dom"

// ■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■

type Props = {
	user: any | null
}

export default function UserLink({ user }: Props) {
	if (!user) return null

	return (
		<Link to={"/admin/users/" + user.id} title="Click to see user's profile.">
			{user.username + " "}
		</Link>
	)
}

